import Foundation

struct UserCredentials: Codable {
    let success: Bool?
    
    struct auth_token: Codable {
        let id: Int
        let key: String
        let expire_time: Int
        let user_id: Int
    }
    
    let error: String?
    let auth_token: auth_token?
}
